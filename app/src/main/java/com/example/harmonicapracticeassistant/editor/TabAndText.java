package com.example.harmonicapracticeassistant.editor;

public class TabAndText
{
    String tabs;
    String text;

    public TabAndText()
    {
        this.tabs = "hello";
        this.text = "hello";
    }

    public TabAndText(String tabs, String text)
    {
        this.tabs = tabs;
        this.text = text;
    }
}
